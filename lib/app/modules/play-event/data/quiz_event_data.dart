  static List<QuizEventModel> getEventsByType(QuizEventType type) {
    return getAllEvents().where((event) => event.type == type).toList();
  }

  static List<QuizEventModel> getEventsByStatus(QuizEventStatus status) {
    return getAllEvents().where((event) => event.status == status).toList();
  }


  static List<QuizEventModel> getAvailableEvents() {
    return getAllEvents().where((event) => 
        event.status != QuizEventStatus.completed && 
        event.currentParticipants < event.maxParticipants).toList();
  }

  static List<QuizEventModel> getUpcomingEvents() {
    return getAllEvents().where((event) => event.isUpcoming).toList();
  }

  static List<QuizEventModel> getOngoingEvents() {
    return getAllEvents().where((event) => event.isOngoing).toList();
  }

  static List<QuizEventModel> getCompletedEvents() {
    return getAllEvents().where((event) => event.isCompleted).toList();
  }
